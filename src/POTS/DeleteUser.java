    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        int selectedRow = userstable.getSelectedRow();
        
        if (selectedRow == -1) {
        // If no user is selected, show a message
        JOptionPane.showMessageDialog(this, "Please select a user to delete.", "Selection Required", JOptionPane.WARNING_MESSAGE);
    } else {
        // Get the username (or User ID) from the selected row (assuming the username is in the 2nd column)
        String usernameToDelete = (String) userstable.getValueAt(selectedRow, 1);  // Column 1 stores the username

        // Confirm deletion
        int confirmation = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete the user: " + usernameToDelete + "?",
            "Confirm Deletion", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirmation == JOptionPane.YES_OPTION) {
            // Proceed with deletion
            deleteUserFromFile(usernameToDelete);

            // Refresh the table to remove the deleted user
            refreshUserTable();

            // Show confirmation message
            JOptionPane.showMessageDialog(this, "User deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_deletebtnActionPerformed
    }    
