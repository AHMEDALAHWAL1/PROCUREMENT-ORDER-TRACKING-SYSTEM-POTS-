    private void loadUsersIntoTable() {
        DefaultTableModel model = (DefaultTableModel) userstable.getModel();
        model.setRowCount(0);  // Clear any existing rows in the table
        
        try (BufferedReader reader = new BufferedReader(new FileReader("Users.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] user = line.split(",");  // Split line into user data (assuming CSV format)
                model.addRow(new Object[]{user[0], user[1], user[2], user[3], user[4]});  // Add the user data into the table
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
     // Refresh the table after deletion
    private void refreshUserTable() {
        DefaultTableModel model = (DefaultTableModel) userstable.getModel();
        model.setRowCount(0);  // Clear the table before reloading data
        
        loadUsersIntoTable();  // Reload users into the table
    }

